<?php

namespace App\Entity;

class CameraApiSettings
{
    protected string $type;

    protected string $path;

    protected string $username;

    protected string $password;

    /** @var String[] $apiSettings */
    public function __construct(array $apiSettings)
    {
        $this->setType($apiSettings['type']);
        $this->setPath($apiSettings['path']);
        $this->setUsername($apiSettings['username']);
        $this->setPassword($apiSettings['password']);
    }

    /**
     * @return string
     */
    public function getType(): string
    {
        return $this->type;
    }

    /**
     * @param string $type
     */
    public function setType(string $type): void
    {
        $this->type = $type;
    }

    /**
     * @return string
     */
    public function getPath(): string
    {
        return $this->path;
    }

    /**
     * @param string $path
     */
    public function setPath(string $path): void
    {
        $this->path = $path;
    }

    /**
     * @return string
     */
    public function getUsername(): string
    {
        return $this->username;
    }

    /**
     * @param string $username
     */
    public function setUsername(string $username): void
    {
        $this->username = $username;
    }

    /**
     * @return string
     */
    public function getPassword(): string
    {
        return $this->password;
    }

    /**
     * @param string $password
     */
    public function setPassword(string $password): void
    {
        $this->password = $password;
    }

}